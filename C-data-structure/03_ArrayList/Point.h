#ifndef __POINT_H__
#define __POINT_H__

// Point 구조체 변수
typedef struct _point {
	int xpos;
	int ypos;
} Point;

// xpos, ypos 값 설정
void SetPointPos(Point* ppos, int xpos, int ypos);

// xpos, ypos 정보 출력
void ShowPointPos(Point* ppos);

// 두 Point 변수 비교
int PointComp(Point* pos1, Point* pos2);

#endif